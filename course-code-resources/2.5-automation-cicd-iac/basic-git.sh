# Basic git commands you may need 
git --version  #gives you your git version

# Create a folder command and change to that directory
mkdir nameofyourfolder
cd nameofyourfolder

# Initialize git
git init

# Create a simple test file
touch filename  ##THIS IS FOR MAC or Linux
echo. > filename ##THIS IS FOR WIN
git status  #check the status of your files
git add filename  #add the file 

# Add multiple files
git add -A

# Commit files to repository
git commit -m "first version"

# Push to repository
git push

# Clode from a Repository
git clone REPOSITORY-URL




